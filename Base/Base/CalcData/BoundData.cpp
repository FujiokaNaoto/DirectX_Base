//**関数***************************************************************************
//	概要	:	コンストラクタ
//*********************************************************************************
CBoundData::CBoundData()
{
	D3DXVECTOR3 vecZero = D3DXVECTOR3(0.0f , 0.0f , 0.0f);
	CBoundData(vecZero , vecZero , 0.0f , 0.0f , 0.0f);
}


//**関数***************************************************************************
//	概要	:	引数つきコンストラクタ
//*********************************************************************************
CBoundData::CBoundData(D3DXVECTOR3& pos , D3DXVECTOR3& velocity , float fMass , float fBounds , float fRadius)
{
	D3DXVECTOR3 vecZero = D3DXVECTOR3(0.0f , 0.0f , 0.0f);

	m_Pos = pos;
	m_Velocity = velocity;

	m_fMass = fMass;
	m_fBounds = fBounds;
	m_fRadius = fRadius;
}
